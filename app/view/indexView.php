<h1>Description</h1>

<p>Il s’agit d’un projet interdisciplinaire, sur les humanités numériques, mené en collaboration avec le CEPAM qui vise à la construction d’un thésaurus de zoologie antique et médiéval. Le but de ce projet consiste à pouvoir marquer des textes anciens
    numérisés, ainsi que des pièces archéologiques répertoriées, pour mettre en évidence
    certains éléments sémantiques.</p> <br/>

<p>Ce projet vise à développer une application qui propose une IHM (interface homme machine)
    simple et intuitive permettant d’associer des mots clés, plus précisément des annotations à des
    unités de texte arbitraires, images, photos et pièces archéologiques etc. Ce projet est basé sur le
    Web sémantique et le Web des données.</p> <br/>

<p>
    Sa fonction principale est de permettre aux utilisateurs et chercheurs de construire une folksonomie et ensuite une ontologie. Ce TER consiste en la réalisation d’un système basé sur le Web
    d’assistance au marquage de matériels textuels et archéologiques.</p>

